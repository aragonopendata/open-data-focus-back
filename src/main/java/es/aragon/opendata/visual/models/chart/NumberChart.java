package es.aragon.opendata.visual.models.chart;

public class NumberChart {

    private String number;
    private String numberOption;
    private String numberColor;
    private String numberUnits;
    private String numberSize;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getNumberOption() {
        return numberOption;
    }

    public void setNumberOption(String numberOption) {
        this.numberOption = numberOption;
    }

    public String getNumberColor() {
        return numberColor;
    }

    public void setNumberColor(String numberColor) {
        this.numberColor = numberColor;
    }

    public String getNumberUnits() {
        return numberUnits;
    }

    public void setNumberUnits(String numberUnits) {
        this.numberUnits = numberUnits;
    }

    public String getNumberSize() {
        return numberSize;
    }

    public void setNumberSize(String numberSize) {
        this.numberSize = numberSize;
    }

}